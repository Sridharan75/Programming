public class AritmeticOperators{
    public static void main(String[] args){
        int m=7,n=5;
        System.out.println("m = "+m+", n = "+n);
        System.out.println("m + n = "+(m+n));
        System.out.println("m - n = "+(m-n));
        System.out.println("m * n = "+(m*n));
        System.out.println("m / n = "+(m/n));
        System.out.println("m % n = "+(m+n));
    }
}